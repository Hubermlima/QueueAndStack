package stack;

public class Cell {
	
    private Integer info;
    private Cell pointer;
	
    public Cell() {}
   
    public Integer getInfo() {
		return info;
	}

	public void setInfo(Integer info) {
		this.info = info;
	}

	public Cell getPointer() {
		return pointer;
	}

	public void setPointer(Cell pointer) {
		this.pointer = pointer;
	}

	
    
    
    
}
